    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        itemID = txtItemID.getText();
        if (itemID.equals("")) {
            JOptionPane.showMessageDialog(this, "Harap isi field", "Information", JOptionPane.INFORMATION_MESSAGE);
            
        }else{
            try {
                int iid = new Integer(id);
                int iitemID = new Integer(itemID);
                String findquery = "SELECT * FROM storage_data WHERE id="+iitemID+" AND user_id="+iid+"";
                rs = stm.executeQuery(findquery);
                if (rs.next()) {
                    query = "DELETE FROM storage_data WHERE id="+iitemID+" AND user_id="+iid+"";
                    stm.executeUpdate(query);
                    JOptionPane.showMessageDialog(this, "Data berhasil di hapus", "Information", JOptionPane.INFORMATION_MESSAGE);
                    back();
                }else{
                    JOptionPane.showMessageDialog(this, "ID barang tidak ditemukan", "Information", JOptionPane.INFORMATION_MESSAGE);
                }
            } catch (HeadlessException | NumberFormatException | SQLException e) {
                JOptionPane.showMessageDialog(this, e, "ERROR", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

